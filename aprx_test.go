func timeFromJD(jd float64) time.Time {
	s, frac := math.Modf((jd - 2440587.5) * 24 * 60 * 60)
	return time.Unix(int64(s), int64(frac*1e9)).UTC()
}

func fmtBC(jd float64) (s string) {
	t := timeFromJD(jd)
	if y := t.Year(); y > 0 {
		s = fmt.Sprintf("%4dAD", y)
	} else {
		s = fmt.Sprintf("%4dBC", 1-y)
	}
	return s + t.Format(" Jan 02 03:04:05")
}

func fmtAD(jd float64) string {
	return timeFromJD(jd).Format("2006 Jan 02 03:04:05")
}

type eclPosFunc func(planet int, jd float64) (x, y, z float64, err error)

func testRange(t *testing.T, pos eclPosFunc, qList []query,
	expErrList [][]float64, jdFmt func(float64) string) {
	t.Log("RA, Dec, and Range values are % of expected error")
	for _, q := range qList {
		t.Log("Planet:", q.planet)
		lines := strings.Split(q.result, "\n")
		n := len(lines) / 2
		for i, j := 0, 0; i < n; i++ {
			var jd, x0, y0, z0 float64
			fmt.Sscan(lines[j], &jd)
			j++
			fmt.Sscan(lines[j], &x0, &y0, &z0)
			j++
			x, y, z, err := pos(q.planet, jd)
			if err != nil {
				t.Log(err)
				continue
			}
			ra1, dec1, rng1 := sky(x, y, z)
			ra0, dec0, rng0 := sky(x0, y0, z0)
			raErr := math.Abs(ra1-ra0) * 60 * 60 * 180 / math.Pi
			decErr := math.Abs(dec1-dec0) * 60 * 60 * 180 / math.Pi
			rngErr := math.Abs(rng1-rng0) * AU * .001
			exp := expErrList[q.planet]
			t.Logf("%s  Pl: %d  RA: %.1f  Dec: %.1f  Range: %.1f", jdFmt(jd),
				q.planet, raErr*100/exp[0], decErr*100/exp[1], rngErr*100/exp[2])
		}
	}
}

const AU = 149597870.691 // km

func sky(x, y, z float64) (ra, dec, rng float64) {
	ra = math.Atan2(x, y)
	rng = math.Sqrt(x*x + y*y + z*z)
	dec = math.Asin(z / rng)
	return
}

